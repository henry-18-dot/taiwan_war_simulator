from __future__ import annotations

from textwrap import dedent


def visual_style_prompt() -> str:
    """Return the canonical visual rendering prompt for the Taiwan map mode.

    This text is intended for artists, UI engineers, and tooling prompts.
    """

    return dedent(
        """
        游戏视觉与渲染风格提示词

        实现一个类似 Flash 风格的实时渲染 2D 游戏画面，整体为高质量像素风（pixel art），
        但具备流畅的动态表现与轻量级动画过渡。画面保持清晰、简洁，强调交互反馈与状态变化。

        核心要求
        1) 地图系统
        - 基于真实地理轮廓构建台湾地图，按区域（城市/县市/战略节点）进行像素化抽象。
        - 地图需要清晰分区、可点击交互、高亮反馈与轻微缩放能力。

        2) 渲染表现
        - 使用实时渲染（非静态切换）。
        - 支持单位移动与区域控制变化的动态过渡。
        - 使用像素级粒子或简单特效（闪烁、路径线、占领扩散）。

        3) 单位与 UI
        - 单位采用简化像素图标，军事/资源/政治势力区分明显。
        - UI 采用扁平像素风，包含轻微动画（按钮按下、选中高亮、数值跳动）。
        - 信息层级保持清晰：地图 > 单位 > UI 叠层。

        4) 交互与动画
        - 玩家选择行动（移动/攻击/占领）后触发小型动画。
        - 动画风格要求快速、明确、低帧但流畅（滑动、闪烁、缩放）。
        - 示例：
          * 攻击 -> 像素爆闪 + 数值弹出
          * 占领 -> 区域颜色渐变填充
          * 移动 -> 路径线 + 单位逐格移动

        5) 氛围与色彩
        - 使用有限调色板（复古策略游戏风格）。
        - 阵营使用明显对比色。
        - 战争氛围通过 UI 提示与颜色变化体现，避免写实血腥表达。

        6) 操作反馈要求（强制）
        - 选择选项时必须有动作反馈（hover/按下/高亮）。
        - 选择选项后必须有确认反馈与持续动画（执行中可观察到过程）。
        - 行动结果必须有结算反馈（数值、控制权、状态变化可视化）。

        目标体验
        - 策略信息清晰。
        - 操作反馈即时。
        - 动画简洁有力。
        - 避免复杂 3D 或高负载效果，保持轻量沉浸。
        """
    ).strip()
